function fileInfo(path) {
  const data = readFileSync(path);
  return {
    path: path.replace(`${process.cwd()}/`, ""),
    exists: true,
    byteSize: statSync(path).size,
    sha256: createHash("sha256").update(data).digest("hex")
  };
}

function hash01(x, y, channel = 0) {
  let n = (x * 374761393 + y * 668265263 + channel * 2246822519 + seed) >>> 0;
  n = Math.imul(n ^ (n >>> 13), 1274126177) >>> 0;
  return ((n ^ (n >>> 16)) >>> 0) / 0xffffffff;
}

function smoothstep(edge0, edge1, value) {
  const t = Math.max(0, Math.min(1, (value - edge0) / (edge1 - edge0)));
  return t * t * (3 - 2 * t);
}

function addNebula(rgb, u, v, cx, cy, sx, sy, color, strength) {
  const dx = (u - cx) / sx;
  const dy = (v - cy) / sy;
  const density = Math.exp(-(dx * dx + dy * dy));
  rgb[0] += color[0] * density * strength;
  rgb[1] += color[1] * density * strength;
  rgb[2] += color[2] * density * strength;
}

function pixelRgb(x, y) {
  const u = x / (width - 1);
  const v = y / (height - 1);
  const horizon = Math.abs(v - 0.52);
  const rgb = [
    0.002 + 0.008 * (1 - v),
    0.004 + 0.011 * (1 - horizon),
    0.011 + 0.025 * (1 - horizon)
  ];

  addNebula(rgb, u, v, 0.18, 0.56, 0.2, 0.12, [0.18, 0.06, 0.48], 0.42);
  addNebula(rgb, u, v, 0.62, 0.48, 0.26, 0.1, [0.02, 0.38, 0.54], 0.34);
  addNebula(rgb, u, v, 0.83, 0.34, 0.16, 0.09, [0.72, 0.32, 0.05], 0.12);

  const dust = Math.pow(hash01(Math.floor(x / 3), Math.floor(y / 3), 1), 5.5) * 0.18;
  rgb[0] += dust * 0.2;
  rgb[1] += dust * 0.42;
  rgb[2] += dust;

  const star = hash01(x, y, 2);
  if (star > 0.999) {
    const hot = 0.08 + 0.28 * smoothstep(0.999, 1, star);
    const tint = hash01(x, y, 3);
    rgb[0] += hot * (tint > 0.72 ? 0.8 : 0.48);
    rgb[1] += hot * (tint > 0.42 ? 0.7 : 0.52);
    rgb[2] += hot;
  }

  return rgb;
}

function toRgbe(rgb) {
  const max = Math.max(rgb[0], rgb[1], rgb[2]);
  if (max < 1e-32) return [0, 0, 0, 0];
  const exponent = Math.ceil(Math.log2(max));
  const scale = 256 / 2 ** exponent;
  return [
    Math.max(0, Math.min(255, Math.round(rgb[0] * scale))),
    Math.max(0, Math.min(255, Math.round(rgb[1] * scale))),
    Math.max(0, Math.min(255, Math.round(rgb[2] * scale))),
    exponent + 128
  ];
}

function encodeChannel(values) {
  const chunks = [];
  let i = 0;
  while (i < values.length) {
    let run = 1;
    while (i + run < values.length && run < 127 && values[i + run] === values[i]) run += 1;
    if (run >= 4) {
      chunks.push(128 + run, values[i]);
      i += run;
      continue;
    }
    const start = i;
    i += 1;
    while (i < values.length) {
      run = 1;
      while (i + run < values.length && run < 127 && values[i + run] === values[i]) run += 1;
      if (run >= 4 || i - start >= 127) break;
      i += 1;
    }
    chunks.push(i - start, ...values.slice(start, i));
  }
  return chunks;
}

