import { chromium } from "@playwright/test";
import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { createGzip, inflateSync } from "node:zlib";
import { createReadStream } from "node:fs";
import { basename, join, resolve } from "node:path";
import { spawn, spawnSync } from "node:child_process";

const [, , promptDirArg] = process.argv;

if (!promptDirArg) {
  console.error("Usage: node capture-run.mjs <prompt-dir>");
  process.exit(2);
}

const promptDir = resolve(promptDirArg);
const sourceDir = join(promptDir, "source");
const metadataFile = join(promptDir, "run-metadata.json");
const metadata = JSON.parse(readFileSync(metadataFile, "utf8"));

function appendNotes(lines) {
  const existing = existsSync(join(promptDir, "notes.md")) ? readFileSync(join(promptDir, "notes.md"), "utf8") : "";
  writeFileSync(join(promptDir, "notes.md"), `${existing.replace(/\s*$/, "")}\n${lines.join("\n")}\n`);
}

function runLogged(command, args, logFile) {
  const result = spawnSync(command, args, {
    cwd: sourceDir,
    encoding: "utf8",
    maxBuffer: 1024 * 1024 * 32
  });
  writeFileSync(
    join(promptDir, logFile),
    [`$ ${command} ${args.join(" ")}`, "", result.stdout ?? "", result.stderr ?? ""].join("\n")
  );
  return result;
}

function walkFiles(root, predicate, acc = []) {
  if (!existsSync(root)) return acc;
  for (const entry of readdirSync(root)) {
    const file = join(root, entry);
    const stat = statSync(file);
    if (stat.isDirectory()) {
      if (!["node_modules", "dist", ".git"].includes(entry)) {
        walkFiles(file, predicate, acc);
      }
    } else if (predicate(file)) {
      acc.push(file);
    }
  }
  return acc;
}

function generatedSourceFiles() {
  return walkFiles(sourceDir, (file) => {
    const rel = file.slice(sourceDir.length + 1);
    if (rel.startsWith("node_modules/") || rel.startsWith("dist/") || rel.startsWith("public/")) return false;
    if (rel === "package.json" || rel === "tsconfig.json") return false;
    return /(^index\.html$|^vite\.config\.[cm]?[tj]s$|^src\/|\.css$|\.ts$|\.tsx$|\.js$|\.jsx$|\.html$)/.test(rel);
  }).sort();
}

function writeSourceListing() {
  const files = generatedSourceFiles();
  const manifest = [];
  const sections = [
    "# Generated Source Listing",
    "",
    "This file is generated by the Round 9 capture runner for neutral scoring.",
    ""
  ];

  for (const file of files) {
    const rel = file.slice(sourceDir.length + 1);
    const text = readFileSync(file, "utf8");
    const loc = text.split(/\r?\n/).filter((line) => line.trim()).length;
    manifest.push({ path: rel, bytes: Buffer.byteLength(text), nonBlankLines: loc });
    sections.push(`## ${rel}`, "", "```", text.replace(/```/g, "'''"), "```", "");
  }

  writeFileSync(join(promptDir, "source-manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`);
  writeFileSync(join(promptDir, "source-listing.md"), `${sections.join("\n").replace(/\s+$/, "")}\n`);
  return { fileCount: files.length, manifest: "source-manifest.json", listing: "source-listing.md" };
}

function countLoc() {
  const files = generatedSourceFiles();
  let loc = 0;
  for (const file of files) {
    const text = readFileSync(file, "utf8");
    for (const line of text.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("//") || trimmed.startsWith("/*") || trimmed.startsWith("*")) continue;
      loc += 1;
    }
  }
  return { loc, files: files.length };
}

async function gzipSize(file) {
  return new Promise((resolveSize, reject) => {
    let total = 0;
    const gzip = createGzip({ level: 9 });
    gzip.on("data", (chunk) => {
      total += chunk.length;
    });
    gzip.on("end", () => resolveSize(total));
    gzip.on("error", reject);
    createReadStream(file).pipe(gzip);
  });
}

async function bundleSize() {
  const jsFiles = walkFiles(join(sourceDir, "dist"), (file) => file.endsWith(".js"));
  let total = 0;
  for (const file of jsFiles) {
    total += await gzipSize(file);
  }
  return total;
}

function detectHallucinatedApisFromBuildLog() {
  const buildLog = existsSync(join(promptDir, "build.log")) ? readFileSync(join(promptDir, "build.log"), "utf8") : "";
  const findings = [];
  const patterns = [
    /Module ['"][^'"]+['"] has no exported member ['"]([^'"]+)['"]/g,
    /Property ['"]?([A-Za-z_$][\w$]*)['"]? does not exist on type/g,
    /Cannot find name ['"]?([A-Za-z_$][\w$]*)['"]?/g,
    /Cannot find module ['"]([^'"]+)['"]/g
  ];
  for (const pattern of patterns) {
    for (const match of buildLog.matchAll(pattern)) {
      findings.push(match[1]);
    }
  }
  return Array.from(new Set(findings)).sort();
}

function buildSummary() {
  const text = existsSync(join(promptDir, "build.log")) ? readFileSync(join(promptDir, "build.log"), "utf8") : "";
  const lines = text
    .split(/\r?\n/)
    .filter((line) => /error|failed|Cannot find|does not exist|has no exported member/i.test(line))
    .slice(0, 20);
  return {
    status: compiles ? "pass" : "fail",
    exitCode: compiles ? 0 : 1,
    keyLines: lines
  };
}

function analyzePngNonblank(buffer) {
  const signature = "89504e470d0a1a0a";
  if (buffer.subarray(0, 8).toString("hex") !== signature) return { nonblank: false, reason: "not a png" };
  let offset = 8;
  let width = 0;
  let height = 0;
  let colorType = 0;
  const idat = [];
  while (offset < buffer.length) {
    const length = buffer.readUInt32BE(offset);
    const type = buffer.subarray(offset + 4, offset + 8).toString("ascii");
    const data = buffer.subarray(offset + 8, offset + 8 + length);
    offset += 12 + length;
    if (type === "IHDR") {
      width = data.readUInt32BE(0);
      height = data.readUInt32BE(4);
      const bitDepth = data[8];
      colorType = data[9];
      if (bitDepth !== 8 || ![2, 6].includes(colorType)) {
        return { nonblank: true, reason: `unsupported png format bitDepth=${bitDepth} colorType=${colorType}` };
      }
    } else if (type === "IDAT") {
      idat.push(data);
    } else if (type === "IEND") {
      break;
    }
  }
  if (!width || !height || idat.length === 0) return { nonblank: false, reason: "missing png data" };
  const channels = colorType === 6 ? 4 : 3;
  const stride = width * channels;
  const inflated = inflateSync(Buffer.concat(idat));
  const rows = [];
  let source = 0;
  let previous = Buffer.alloc(stride);
  for (let y = 0; y < height; y += 1) {
    const filter = inflated[source++];
    const row = Buffer.from(inflated.subarray(source, source + stride));
    source += stride;
    for (let x = 0; x < stride; x += 1) {
      const left = x >= channels ? row[x - channels] : 0;
      const up = previous[x] ?? 0;
      const upLeft = x >= channels ? previous[x - channels] : 0;
      if (filter === 1) row[x] = (row[x] + left) & 255;
      else if (filter === 2) row[x] = (row[x] + up) & 255;
      else if (filter === 3) row[x] = (row[x] + Math.floor((left + up) / 2)) & 255;
      else if (filter === 4) {
        const p = left + up - upLeft;
        const pa = Math.abs(p - left);
        const pb = Math.abs(p - up);
        const pc = Math.abs(p - upLeft);
        row[x] = (row[x] + (pa <= pb && pa <= pc ? left : pb <= pc ? up : upLeft)) & 255;
      }
    }
    rows.push(row);
    previous = row;
  }
  const sampleStepX = Math.max(1, Math.floor(width / 48));
  const sampleStepY = Math.max(1, Math.floor(height / 32));
  const colors = new Set();
  let minLuma = 255;
  let maxLuma = 0;
  for (let y = 0; y < height; y += sampleStepY) {
    const row = rows[y];
    for (let x = 0; x < width; x += sampleStepX) {
      const index = x * channels;
      const r = row[index];
      const g = row[index + 1];
      const b = row[index + 2];
      const a = channels === 4 ? row[index + 3] : 255;
      if (a < 8) continue;
      const luma = Math.round(0.2126 * r + 0.7152 * g + 0.0722 * b);
      minLuma = Math.min(minLuma, luma);
      maxLuma = Math.max(maxLuma, luma);
      colors.add(`${r >> 4},${g >> 4},${b >> 4}`);
    }
  }
  return {
    nonblank: colors.size >= 3 && maxLuma - minLuma >= 12,
    width,
    height,
    sampledColorBuckets: colors.size,
    lumaRange: maxLuma - minLuma
  };
}

function detectInventedAssetPaths() {
  if (!metadata.promptFile.includes("10-product-viewer")) return 0;
  const files = generatedSourceFiles();
  const invented = new Set();
  const allowed = new Set(["benchmark/assets/sneaker.glb"]);
  const glbPattern = /(?:\.\.?\/|\/)?[A-Za-z0-9_.~/-]+\.glb/g;
  for (const file of files) {
    const text = readFileSync(file, "utf8");
    for (const match of text.matchAll(glbPattern)) {
      const normalized = match[0].replace(/^(\.\/|\.\.\/|\/)+/, "");
      if (!allowed.has(normalized)) invented.add(match[0]);
    }
  }
  writeFileSync(join(promptDir, "asset-audit.json"), `${JSON.stringify({ invented: Array.from(invented).sort() }, null, 2)}\n`);
  return invented.size;
}

function readRepairTurns() {
  if (Number.isFinite(metadata.repairTurns)) return metadata.repairTurns;
  const notesPath = join(promptDir, "notes.md");
  if (!existsSync(notesPath)) return 0;
  const notes = readFileSync(notesPath, "utf8");
  const match = notes.match(/repair turns:\s*(\d+)/i);
  return match ? Number(match[1]) : 0;
}

async function captureBrowser(port) {
  const server = spawn("npm", ["run", "dev", "--", "--port", String(port)], {
    cwd: sourceDir,
    stdio: ["ignore", "pipe", "pipe"]
  });
  let runLog = "";
  server.stdout.on("data", (chunk) => {
    runLog += chunk.toString();
  });
  server.stderr.on("data", (chunk) => {
    runLog += chunk.toString();
  });

  const started = Date.now();
  let browser;
  try {
    browser = await chromium.launch();
    const page = await browser.newPage({ viewport: { width: 1440, height: 960 }, deviceScaleFactor: 1 });
    const consoleErrors = [];
    const pageErrors = [];
    const requestFailures = [];
    page.on("console", (message) => {
      if (message.type() === "error") consoleErrors.push(message.text());
    });
    page.on("pageerror", (error) => {
      pageErrors.push(error.message);
    });
    page.on("requestfailed", (request) => {
      requestFailures.push(`${request.method()} ${request.url()} ${request.failure()?.errorText ?? ""}`.trim());
    });
    const url = `http://127.0.0.1:${port}/`;
    let loaded = false;
    let lastError = "";
    for (let i = 0; i < 80; i += 1) {
      try {
        const response = await page.goto(url, { waitUntil: "domcontentloaded", timeout: 3000 });
        if (response && response.ok()) {
          loaded = true;
          break;
        }
      } catch (error) {
        lastError = error instanceof Error ? error.message : String(error);
        await new Promise((resolveDelay) => setTimeout(resolveDelay, 250));
      }
    }
    if (!loaded) {
      throw new Error(`page did not load: ${lastError}`);
    }
    let firstUsableRenderMs = null;
    let route = null;
    for (let i = 0; i < 40; i += 1) {
      route = await page.evaluate(() => {
        const canvas = Array.from(document.querySelectorAll("canvas")).map((node) => {
          const rect = node.getBoundingClientRect();
          return { width: rect.width, height: rect.height };
        });
        const text = document.body.innerText.slice(0, 2000);
        const hasVisibleCanvas = canvas.some((entry) => entry.width >= 100 && entry.height >= 100);
        return {
          status: hasVisibleCanvas || document.body.innerText.trim().length > 0 ? "pass" : "fail",
          canvasCount: canvas.length,
          canvas,
          bodyTextLength: document.body.innerText.trim().length,
          bodyTextSample: text
        };
      });
      if (route.status === "pass") {
        firstUsableRenderMs = Date.now() - started;
        break;
      }
      await page.waitForTimeout(250);
    }
    await page.waitForTimeout(10000);
    const screenshotPath = join(promptDir, "screenshot.png");
    const screenshotBuffer = await page.screenshot({ path: screenshotPath, fullPage: false, timeout: 60000 });
    const screenshotAnalysis = analyzePngNonblank(screenshotBuffer);
    const promptNumber = Number(metadata.promptFile.match(/prompts\/(\d\d)-/)?.[1] ?? "0");
    const motionEvidencePrompts = new Set([1, 2, 4, 6, 9, 10]);
    const runtimeEvidence = [];
    if (motionEvidencePrompts.has(promptNumber)) {
      await page.waitForTimeout(1500);
      const secondScreenshot = "screenshot-02.png";
      await page.screenshot({ path: join(promptDir, secondScreenshot), fullPage: false, timeout: 60000 });
      runtimeEvidence.push({
        path: secondScreenshot,
        reason: "second settled frame for motion/interaction evidence",
        delayAfterPrimaryMs: 1500
      });
    }
    route = route ?? { status: "fail", canvasCount: 0, canvas: [], bodyTextLength: 0, bodyTextSample: "" };
    route.consoleErrors = consoleErrors;
    route.pageErrors = pageErrors;
    route.requestFailures = requestFailures;
    route.screenshotAnalysis = screenshotAnalysis;
    route.routeHealthMethod = "HTTP 200 + visible canvas/DOM poll + screenshot PNG nonblank analysis";
    route.url = url;
    route.runCommand = `npm run dev -- --port ${port}`;
    if (pageErrors.length > 0) route.status = "fail";
    if (!screenshotAnalysis.nonblank) route.status = "fail";
    writeFileSync(join(promptDir, "route-health.json"), `${JSON.stringify(route, null, 2)}\n`);
    writeFileSync(join(promptDir, "run.log"), runLog);
    return { runsInBrowser: route.status === "pass", screenshot: "screenshot.png", route, firstUsableRenderMs, runtimeEvidence };
  } finally {
    if (browser) await browser.close();
    server.kill("SIGTERM");
    writeFileSync(join(promptDir, "run.log"), runLog);
  }
}

const install = runLogged("npm", ["install"], "install.log");
let compiles = false;
let runsInBrowser = false;
let screenshot = null;
let firstUsableRenderMs = null;
let runError = null;
let routeHealth = {
  status: "fail",
  stage: "not-run",
  reason: "Runtime capture did not run."
};
let runtimeEvidence = [];

if (install.status === 0) {
  const build = runLogged("npm", ["run", "build"], "build.log");
  compiles = build.status === 0;
  if (compiles) {
    const promptNumber = Number(metadata.promptFile.match(/prompts\/(\d\d)-/)?.[1] ?? "0");
    const runIndex = ["codex-aura3d", "codex-threejs", "claude-aura3d", "claude-threejs"].indexOf(metadata.runId);
    const port = 6100 + runIndex * 100 + promptNumber;
    try {
      const captured = await captureBrowser(port);
      runsInBrowser = captured.runsInBrowser;
      screenshot = captured.screenshot;
      firstUsableRenderMs = captured.firstUsableRenderMs;
      routeHealth = captured.route;
      runtimeEvidence = captured.runtimeEvidence;
    } catch (error) {
      runError = error instanceof Error ? error.message : String(error);
      routeHealth = {
        status: "fail",
        stage: "runtime-capture",
        reason: runError
      };
      writeFileSync(join(promptDir, "route-health.json"), `${JSON.stringify(routeHealth, null, 2)}\n`);
      writeFileSync(join(promptDir, "run.log"), `${existsSync(join(promptDir, "run.log")) ? readFileSync(join(promptDir, "run.log"), "utf8") : ""}\n${runError}\n`);
    }
  } else {
    routeHealth = {
      status: "fail",
      stage: "build",
      reason: "npm run build failed"
    };
    writeFileSync(join(promptDir, "route-health.json"), `${JSON.stringify(routeHealth, null, 2)}\n`);
  }
} else {
  routeHealth = {
    status: "fail",
    stage: "install",
    reason: "npm install failed"
  };
  writeFileSync(join(promptDir, "route-health.json"), `${JSON.stringify(routeHealth, null, 2)}\n`);
  writeFileSync(join(promptDir, "build.log"), "Build not attempted because npm install failed.\n");
}

const { loc, files } = countLoc();
const gzipBytes = compiles ? await bundleSize() : null;
const sourceEvidence = writeSourceListing();
const promptSlug = basename(metadata.promptFile, ".md");
const hallucinatedApis = detectHallucinatedApisFromBuildLog();
const metrics = {
  prompt: promptSlug,
  agent: metadata.agent,
  library: metadata.library,
  compiles,
  runsInBrowser,
  routeHealth: routeHealth.status,
  routeUrl: runsInBrowser ? routeHealth.url ?? null : null,
  runCommand: runsInBrowser ? routeHealth.runCommand ?? null : "npm run dev -- --port <assigned-port>",
  routeHealthEvidence: "route-health.json",
  linesOfUserCode: loc,
  filesCreated: files,
  hallucinatedApis: hallucinatedApis.length,
  hallucinatedApiFindings: hallucinatedApis,
  inventedAssetPaths: detectInventedAssetPaths(),
  repairTurns: readRepairTurns(),
  timeToFirstUsableRenderMs: runsInBrowser
    ? (metadata.agentDurationMs ?? 0) + (firstUsableRenderMs ?? 0)
    : null,
  bundleSizeGzipBytes: gzipBytes,
  buildSummary: buildSummary(),
  screenshot,
  runtimeEvidence,
  sourceEvidence
};

writeFileSync(join(promptDir, "metrics.json"), `${JSON.stringify(metrics, null, 2)}\n`);
appendNotes([
  "",
  `capture timestamp: ${new Date().toISOString()}`,
  `status: ${compiles && runsInBrowser ? "captured" : "failed"}`,
  `failure stage: ${install.status !== 0 ? "install" : !compiles ? "build" : !runsInBrowser ? "runtime" : "none"}`,
  `agent exit code: ${metadata.agentExitCode ?? "not-run"}`,
  `install status: ${install.status}`,
  `compile status: ${compiles ? "pass" : "fail"}`,
  `browser status: ${runsInBrowser ? "pass" : "fail"}`,
  `route URL: ${routeHealth.url ?? "none"}`,
  `run command used: ${routeHealth.runCommand ?? "none"}`,
  `route health: ${routeHealth.status}`,
  `screenshot timestamp: ${screenshot ? new Date().toISOString() : "none"}`,
  runError ? `runtime failure: ${runError}` : "runtime failure: none"
]);

if (!compiles || !runsInBrowser) {
  process.exit(1);
}
